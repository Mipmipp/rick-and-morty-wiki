export default class Character {
    constructor(id, name, status, specie, gender, origin, location, image) {
        this.id = id;
        this.name = name;
        this.status = status;
        this.specie = specie;
        this.gender = gender;
        this.origin = origin;
        this.location = location;
        this.image = image;
    }
}
